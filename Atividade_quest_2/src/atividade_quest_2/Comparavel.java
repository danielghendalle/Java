
package atividade_quest_2;

public class Comparavel {
    
    
    public static void MaiorOuIgual(double num){
    
    if(num>=10){
        System.out.println("A comparável é maior ou igual a 10!");
    }
    else{
        System.out.println("False");
    }
    }
    public static void MenorOuIgual(double num){
      if(num<=10){
        System.out.println("A comparável é menor ou igual a 10!");
    }
    else{
        System.out.println("O valor não é menor do que 10");
    }
    
    }
     public static void DiferenteDe(double num){
      if(num !=10){
        System.out.println("A comparável é diferente de 10!");
    }
    else{
        System.out.println("False");
    }
    
    }
}

